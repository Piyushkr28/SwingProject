    public void StudentLoad()
    {
        try {
            pst = con.prepareStatement("select student_no from student");
            rs = pst.executeQuery();
            s_id.removeAllItems();
            while(rs.next())
            {
                s_id.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(InsertStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
        
