    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            //insert code
            String reg = s_no.getText();
            String name = s_name.getText();
            String dob = s_dob.getText();
            String doj = s_doj.getText();
            
            if(reg.isEmpty() || name.isEmpty() || dob.isEmpty() || doj.isEmpty())
            {
                JOptionPane.showMessageDialog(this,"Failed to Insert, All fields are required");
                StudentLoad();
            }
            
            pst = con.prepareStatement("insert into student (student_no,student_name,student_dob,student_doj) values(?,?,?,?)");
            
            pst.setString(1,reg);
            pst.setString(2,name);
            pst.setString(3,dob);
            pst.setString(4,doj);
            
            int q = pst.executeUpdate();
            if(q == 1)
            {
                JOptionPane.showMessageDialog(this,"Record Added!"); 
                s_no.setText("");
                s_name.setText("");
                s_dob.setText("");
                s_doj.setText("");
                s_no.requestFocus();
                StudentLoad();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Failed to Insert!");
                //StudentLoad();
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(InsertStudent.class.getName()).log(Level.SEVERE, null, ex);
            //StudentLoad();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

